package ghozti.schoolDBsystem.student;

public class Student {

    private String name;
    private int age;
    private String birthDate;
    private int GPA;

    public String getName(){
        return name;
    }

    public int getAge(){
        return age;
    }

    public String getBirthDate(){
        return birthDate;
    }

    public int getGPA(){
        return GPA;
    }
}
